package com.spring.model;

public class TicketCount {
	private int open;
	private int closed;
	private int reopen;
	
	public int getOpen() {
		return open;
	}
	public void setOpen(int open) {
		this.open = open;
	}
	public int getClosed() {
		return closed;
	}
	public void setClosed(int closed) {
		this.closed = closed;
	}
	public int getReopen() {
		return reopen;
	}
	public void setReopen(int reopen) {
		this.reopen = reopen;
	}
}
